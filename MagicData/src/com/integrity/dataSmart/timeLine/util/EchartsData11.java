package com.integrity.dataSmart.timeLine.util;

import java.util.ArrayList;
import java.util.List;

public class EchartsData11 {
	public Integer dateYear;
	public List<String> title = new ArrayList<String>();
	public List<String> legend = new ArrayList<String>();
	public List<String> xcategory = new ArrayList<String>();// x轴数据
	public List<List<Long>> series = new ArrayList<List<Long>>();

    
	public EchartsData11() {
		super();
	}


	public EchartsData11(Integer dateYear, List<String> title,
			List<String> legend, List<String> xcategory, List<List<Long>> series) {
		super();
		this.dateYear = dateYear;
		this.title = title;
		this.legend = legend;
		this.xcategory = xcategory;
		this.series = series;
	}


	public Integer getDateYear() {
		return dateYear;
	}


	public void setDateYear(Integer dateYear) {
		this.dateYear = dateYear;
	}


	public List<String> getTitle() {
		return title;
	}


	public void setTitle(List<String> title) {
		this.title = title;
	}


	public List<String> getLegend() {
		return legend;
	}


	public void setLegend(List<String> legend) {
		this.legend = legend;
	}


	public List<String> getXcategory() {
		return xcategory;
	}


	public void setXcategory(List<String> xcategory) {
		this.xcategory = xcategory;
	}


	public List<List<Long>> getSeries() {
		return series;
	}


	public void setSeries(List<List<Long>> series) {
		this.series = series;
	}


	@Override
	public String toString() {
		return "EchartsData11 [dateYear=" + dateYear + ", title=" + title
				+ ", legend=" + legend + ", xcategory=" + xcategory
				+ ", series=" + series + "]";
	}



}
