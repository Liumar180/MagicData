package com.integrity.dataSmart.timeLine.util;

import java.util.ArrayList;
import java.util.List;

public class BaseDataList {
	
	 public List<Long> series1 = new ArrayList<Long>();//生成曲线图数据
	 public List<Long> series2 = new ArrayList<Long>();//生成曲线图数据
	 public List<Long> series3 = new ArrayList<Long>();//生成曲线图数据
	public BaseDataList() {
		super();
	}
	public BaseDataList(List<Long> series1, List<Long> series2,
			List<Long> series3) {
		super();
		this.series1 = series1;
		this.series2 = series2;
		this.series3 = series3;
	}
	public List<Long> getSeries1() {
		return series1;
	}
	public void setSeries1(List<Long> series1) {
		this.series1 = series1;
	}
	public List<Long> getSeries2() {
		return series2;
	}
	public void setSeries2(List<Long> series2) {
		this.series2 = series2;
	}
	public List<Long> getSeries3() {
		return series3;
	}
	public void setSeries3(List<Long> series3) {
		this.series3 = series3;
	}
	@Override
	public String toString() {
		return "BaseDataList [series1=" + series1 + ", series2=" + series2
				+ ", series3=" + series3 + "]";
	}
	
	
}
