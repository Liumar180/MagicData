package com.integrity.dataSmart.pojo;

/**
 * @author integrity
 * 天猫数据实体
 */
public class TianmaoData {
	
	private String name;//姓名
	private String phone;//电话
	private String qq;//QQ号
	private String address;//地址
	private String email;//邮箱
	private String username;//用户名
	private String password;//密码
	private String info_rowid;//数据源id
	private String domain;//数据来源
	private String info_type;//数据类型（来源）
	private String info_table;//表
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getQq() {
		return qq;
	}
	public void setQq(String qq) {
		this.qq = qq;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getInfo_rowid() {
		return info_rowid;
	}
	public void setInfo_rowid(String info_rowid) {
		this.info_rowid = info_rowid;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	
	public String getInfo_type() {
		return info_type;
	}
	public void setInfo_type(String info_type) {
		this.info_type = info_type;
	}
	public String getInfo_table() {
		return info_table;
	}
	public void setInfo_table(String info_table) {
		this.info_table = info_table;
	}
	@Override
	public String toString() {
		return "TianmaoData [name=" + name + ", phone=" + phone + ", qq=" + qq
				+ ", address=" + address + ", email=" + email + ", username="
				+ username + ", password=" + password + ", info_rowid="
				+ info_rowid + ", domain=" + domain + ", info_type="
				+ info_type + ", info_table=" + info_table + "]";
	}

}
