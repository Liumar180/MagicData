package com.integrity.dataSmart.pojo;

import java.util.List;
import java.util.Map;

public class VertexObject {

	/**
	 * 节点类型
	 */
	private String type;
	
	/**
	 * 基础属性map 
	 * 节点自身的属性
	 * key 节点属性
	 * value 节点属性值
	 */
	private Map<String, String> baseProMap;
	
	/**
	 * 人物节点的own属性节点
	 * T own节点自身属性map
	 */
	private List<Map<String, String>> ownProList;

	public String getType() {
		return type;
	}

	public Map<String, String> getBaseProMap() {
		return baseProMap;
	}

	public List<Map<String, String>> getOwnProList() {
		return ownProList;
	}

	public void setType(String type) {
		this.type = type;
	}

	public void setBaseProMap(Map<String, String> baseProMap) {
		this.baseProMap = baseProMap;
	}

	public void setOwnProList(List<Map<String, String>> ownProList) {
		this.ownProList = ownProList;
	}
	

	
}
