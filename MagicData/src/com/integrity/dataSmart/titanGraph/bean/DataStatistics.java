package com.integrity.dataSmart.titanGraph.bean;

import java.math.BigInteger;
import java.util.Date;

/**
 * @author liuBf
 * 数据统计实体类
 *
 */
public class DataStatistics {
	private Long id;
	private String createPerson;//创建人
	private String dataDescription;//数据描述
    private String dataType;//数据类型
    private String labelName;//数据名称、标签
    private String area;//地域
    private BigInteger dataAcount;//数据量
    private Date bornTime;//创建时间
    
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCreatePerson() {
		return createPerson;
	}
	public void setCreatePerson(String createPerson) {
		this.createPerson = createPerson;
	}
	public String getDataDescription() {
		return dataDescription;
	}
	public void setDataDescription(String dataDescription) {
		this.dataDescription = dataDescription;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getLabelName() {
		return labelName;
	}
	public void setLabelName(String labelName) {
		this.labelName = labelName;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public BigInteger getDataAcount() {
		return dataAcount;
	}
	public void setDataAcount(BigInteger dataAcount) {
		this.dataAcount = dataAcount;
	}
	public Date getBornTime() {
		return bornTime;
	}
	public void setBornTime(Date bornTime) {
		this.bornTime = bornTime;
	}
    

}
