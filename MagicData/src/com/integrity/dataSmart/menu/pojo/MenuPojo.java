package com.integrity.dataSmart.menu.pojo;

import java.util.List;

public class MenuPojo {

	//菜单链接地址
	String hrefString = "";
	//显示角度
	String angleString = "";
	//图标类名称
	String icoClassString = "";

	//是否包含子类
	Boolean hasSubFlag= false;
	
	List<MenuPojo> subMenuPojos;
	
	public String getHrefString() {
		return hrefString;
	}
	public void setHrefString(String hrefString) {
		this.hrefString = hrefString;
	}
	public String getAngleString() {
		return angleString;
	}
	public void setAngleString(String angleString) {
		this.angleString = angleString;
	}
	
	public Boolean getHasSubFlag() {
		return hasSubFlag;
	}
	public void setHasSubFlag(Boolean hasSubFlag) {
		this.hasSubFlag = hasSubFlag;
	}
	public String getIcoClassString() {
		return icoClassString;
	}
	public void setIcoClassString(String icoClassString) {
		this.icoClassString = icoClassString;
	}
	public List<MenuPojo> getSubMenuPojos() {
		return subMenuPojos;
	}
	public void setSubMenuPojos(List<MenuPojo> subMenuPojos) {
		this.subMenuPojos = subMenuPojos;
	}	
}
